export function getGameQuestions() {
  // Returns 15 questions: 3 from each difficulty level, randomly selected
  // Shuffled into ascending difficulty order for the game
  const byDiff = [1, 2, 3, 4, 5].map(d => QUESTIONS.filter(q => q.difficulty === d))
  const selected = byDiff.flatMap(group => {
    const shuffled = [...group].sort(() => Math.random() - 0.5)
    return shuffled.slice(0, 3)
  })
  return selected // already ordered easy→hard (3+3+3+3+3)
}
